package exercises7;

public class Book {

	private String name;
	private int lenght;
	private int year;

public Dog(String name, int lenght, int year) {
	this.name = name;
	this.lenght = lenght;
	this.year = year;
	}

	public String getName() {
		return name;
	}

	public int getLength() {
		return lenght;
	}

	public int getAge() {
		return year;
	}
	
	void year() {
		this.year++;
	}
	

}
